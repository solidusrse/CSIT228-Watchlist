    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here
        String genre = tfGenre.getText().toString();
        String title = tfTitle.getText().toString();
        String synopsis = tfSynopsis.getText().toString();
        int episodes = Integer.parseInt(tfEpisodes.getText());
        String type;
        if(cbType.getSelectedIndex()==0)
            type = "Movie";
        else
            type = "Series";
        if(!title.isBlank() && episodes > 0 && !genre.isBlank() && !synopsis.isBlank()){
            MovieSeries mvs = new MovieSeries(title, episodes, type, genre, synopsis);
            conn.addMovieSeries(mvs);
        }
    }//GEN-LAST:event_btnAddActionPerformed

